// Helper function to get orphanages by location
export const getOrphanagesByLocation = (location) => {
  return orphanagesData.filter((orphanage) => orphanage.location === location)
}

// Helper function to get all locations with orphanage counts
export const getLocationsWithCounts = () => {
  const locationCounts = {}
  orphanagesData.forEach((orphanage) => {
    if (!locationCounts[orphanage.location]) {
      locationCounts[orphanage.location] = {
        label: orphanage.locationLabel,
        count: 0,
      }
    }
    locationCounts[orphanage.location].count++
  })
  return locationCounts
}

// Helper function to find orphanage by ID
export const getOrphanageById = (id) => {
  return orphanagesData.find((orphanage) => orphanage.id === id)
}
